package com.deliverystatus;

public class DeliveryStatusDTO {
	private String dsNo;
	private String dsDate;
	private String ds_manage;
	private String orderNo;
	
	public String getDsNo() {
		return dsNo;
	}
	public void setDsNo(String dsNo) {
		this.dsNo = dsNo;
	}
	public String getDsDate() {
		return dsDate;
	}
	public void setDsDate(String dsDate) {
		this.dsDate = dsDate;
	}
	public String getDs_manage() {
		return ds_manage;
	}
	public void setDs_manage(String ds_manage) {
		this.ds_manage = ds_manage;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	
	
}
